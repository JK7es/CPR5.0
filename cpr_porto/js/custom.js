function getTextoMes(mes){
	switch (mes) {
		case 1:
			txtMes = "Enero";
			break;
		case 2:
			txtMes = "Febrero";
			break;
		case 3:
			txtMes = "Marzo";
			break;
		case 4:
			txtMes = "Abril";
			break;
		case 5:
			txtMes = "Mayo";
			break;
		case 6:
			txtMes = "Junio";
			break;
		case 7:
			txtMes = "Julio";
			break;
		case 8:
			txtMes = "Agosto";
			break;
		case 9:
			txtMes = "Septiembre";
			break;
		case 10:
			txtMes = "Octubre";
			break;
		case 11:
			txtMes = "Noviembre";
			break;
		case 12:
			txtMes = "Diciembre";
			break;	
	}
	return txtMes;
}